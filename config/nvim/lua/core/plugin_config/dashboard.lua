function M.get_sections()
  local header = {
    type = "text",
    val = banner,
    opts = {
      position = "center",
      hl = "Label",
    },
  }

  local footer = {
    type = "text",
    val = text.align_center({ width = 0 }, {
      "",
      "lunarvim.org",
    }, 0.5),
    opts = {
      position = "center",
      hl = "Number",
    },
  }

  local buttons = {
    opts = {
      hl_shortcut = "Include",
      spacing = 1,
    },
    entries = {
      { "f", ":)" ..  "Find File", "<CMD>Telescope find_files<CR>" },
      { "n", ":O" .. "  New File", "<CMD>ene!<CR>" },
      { "p", ":D" .. "  Projects ", "<CMD>Telescope projects<CR>" },
      { "r", ":P" .. "  Recent files", ":Telescope oldfiles <CR>" },
      { "t", ":>" .. "  Find Text", "<CMD>Telescope live_grep<CR>" },
      { "q", ":<" .. "  Quit", "<CMD>quit<CR>" },
    },
  }
  return {
    header = header,
    buttons = buttons,
    footer = footer,
  }
end

return M
